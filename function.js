// ==========================
// FUNCIÓN SIMPLE (sin parámetros)
// Muestra mensajes en consola
// ==========================
function hello() {
    console.log('hello berito');
    console.log('trabajando en batea');
}

hello();


// ==========================
// FUNCIÓN CON RETURN
// Retorna un texto
// ==========================
function hola() {
    return 'hola mundo';
}

const resultado = hola();
console.log(resultado);


// ==========================
// FUNCIÓN MATEMÁTICA
// Retorna una suma de números
// ==========================
function hello() {
    return 100000 + 200000;
}

console.log(hello());


// ==========================
// FUNCIÓN CON PARÁMETROS
// Recibe un nombre y lo concatena
// ==========================
function hello(name) {
    return 'hello ' + name;
}

console.log(hello("berito"));
console.log(hello("dario"));
console.log(hello("batea"));
console.log(hello("adrian"));


// ==========================
// FUNCIÓN DE SUMA
// Recibe dos parámetros y suma
// ==========================
function add(x, y) {
    return x + y;
}

console.log(add(200, 10));
console.log(add(100, 10));
console.log(add(50, 10));